echo '----[Host]----------------------------------------------------------------------'
uname -a

echo ''
echo '----[Shell]---------------------------------------------------------------------'
echo $$DOC_ROOT$$SHELL_FILE

echo ''
echo '----[Priv]----------------------------------------------------------------------'
id

echo ''
echo '----[Uptime]--------------------------------------------------------------------'
uptime|sed -e 's/ //'

echo ''
echo '----[Memory]--------------------------------------------------------------------'
free -m

echo ''
echo '----[Disk]----------------------------------------------------------------------'
df -H

echo ''
echo '----[Dir]-----------------------------------------------------------------------'
ls -ltr

echo ''
echo '----[Processes]-----------------------------------------------------------------'
for user in `who|cut -d' ' -f1`
	do ps -u "$user" -o user,args,pid,ppid
done

echo ''
echo '----[Networks]------------------------------------------------------------------'
`whereis ifconfig|awk '{print $2}'` -a|grep -E '^(\w|\s+inet|$)'|head -n -1

echo ''
echo '----[Routing]-------------------------------------------------------------------'
`whereis route|awk '{print $2}'` -n

echo ''
echo '----[ARP Cache]-----------------------------------------------------------------'
`whereis arp|awk '{print $2}'` -n

echo ''
echo '----[TCP Connections]-----------------------------------------------------------'
netstat -antp 2>/dev/null

echo ''
echo '----[Logged In]-----------------------------------------------------------------'
w|tail -n +2

echo ''
echo '----[Last Login]----------------------------------------------------------------'
lastlog|grep -vE 'Never'

if [ -d /var/cache/apt/ ]
then echo ''
	echo '----[Last Update]---------------------------------------------------------------'
	stat -c %y /var/cache/apt/
fi

echo ''
echo '----[Firewall]------------------------------------------------------------------'
`whereis iptables|awk '{print $2}'` -L -t nat
